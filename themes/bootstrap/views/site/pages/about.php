<h3>About</h3>
</br>
<p>Nowadays students that seek answers for school related questions usually use search engines as their main source of information;
    but in most cases this information is unreliable and incomplete. This project was envisioned to fulfill this need; to allow
    students to communicate with mentors that are experts in their field. In our mentoring platform students can post a question of
    any subject and it will be assigned to the most expertise domain mentor to get the most reliable and accurate answer. In senior
    project class, these platforms could be use for communication within team members and their mentors.</p>
